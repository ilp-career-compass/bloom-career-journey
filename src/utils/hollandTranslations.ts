export function getTranslatedHollandQuestion(sequenceNumber: number, lang: string, defaultText: string): string {
  const language = (['en', 'kn', 'ta', 'hi'].includes(lang) ? lang : 'en') as Lang;
  const translated = HOLLAND_QUESTIONS_TRANSLATIONS[language]?.[sequenceNumber];
  return translated || defaultText;
}

export function getTranslatedHollandQuestionByText(englishText: string, lang: string): string {
  const language = (['en', 'kn', 'ta', 'hi'].includes(lang) ? lang : 'en') as Lang;
  if (language === 'en') return englishText;

  // Find sequence number of englishText in the english table
  const enQuestions = HOLLAND_QUESTIONS_TRANSLATIONS.en;
  const entry = Object.entries(enQuestions).find(([_, text]) => text.toLowerCase().trim() === englishText.toLowerCase().trim());
  if (entry) {
    const seqNum = Number(entry[0]);
    return HOLLAND_QUESTIONS_TRANSLATIONS[language]?.[seqNum] || englishText;
  }
  
  // Fallback: search for partial match if database text changed slightly
  const partialEntry = Object.entries(enQuestions).find(([_, text]) => 
    text.toLowerCase().includes(englishText.toLowerCase()) || englishText.toLowerCase().includes(text.toLowerCase())
  );
  if (partialEntry) {
    const seqNum = Number(partialEntry[0]);
    return HOLLAND_QUESTIONS_TRANSLATIONS[language]?.[seqNum] || englishText;
  }

  return englishText;
}

